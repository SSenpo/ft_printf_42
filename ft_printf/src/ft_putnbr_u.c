#include "ft_printf.h"

int	ft_putnbr_u(unsigned int n)
{
	int	len;

	len = 0;
	if (n >= 10)
	{
		len += ft_putnbr_fd(n / 10);
	}
	len += ft_putchar_fd(n % 10 + '0');
	return (len);
}
